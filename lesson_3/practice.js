function longestConsec(wordArr, numStrings) {
  if (numStrings < 0) return "";
  let combinations = [];
  for (let i = 0; i < wordArr.length - numStrings + 1; i += 1) {
    combinations.push([i, wordArr.slice(i, i + numStrings).reduce((accum, elem) => accum + elem.length, 0)]);
  }
  if (combinations.length === 0) return "";
  combinations.sort((a, b) => (a[1] === b[1] ? a[0] - b[0] : b[1] - a[1]));
  let firstIndex = combinations[0][0];
  return wordArr.slice(firstIndex, firstIndex + numStrings).join('');
}

